import java.util.Scanner;

public class Exercise03CalculateDifference {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        int A, B, C, D, difference;

        A = scanner.nextInt();
        B = scanner.nextInt();
        C = scanner.nextInt();
        D = scanner.nextInt();

        difference = A * B - C * D;

        System.out.println("DIFFERENCE = " + difference);

        sc.close();
    }
}

